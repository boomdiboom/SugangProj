package sugang;

public class Professor {
	private String profid;
	private String profpw;
	private String pname;
	private String ptell;
	private String majid;
	private String college;
	
	public String getProfpw() {
		return profpw;
	}
	public void setProfpw(String profpw) {
		this.profpw = profpw;
	}
	public String getProfid() {
		return profid;
	}
	public void setProfid(String profid) {
		this.profid = profid;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPtell() {
		return ptell;
	}
	public void setPtell(String ptell) {
		this.ptell = ptell;
	}
	public String getMajid() {
		return majid;
	}
	public void setMajid(String majid) {
		this.majid = majid;
	}
	public String getCollege() {
		return college;
	}
	public void setCollege(String college) {
		this.college = college;
	}

}
