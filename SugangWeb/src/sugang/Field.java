package sugang;

public class Field {
	private String fieid;
	private String fname;
	private String majid;
	private int fienum;
	public String getFieid() {
		return fieid;
	}
	public void setFieid(String fieid) {
		this.fieid = fieid;
	}
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getMajid() {
		return majid;
	}
	public void setMajid(String majid) {
		this.majid = majid;
	}
	public int getFienum() {
		return fienum;
	}
	public void setFienum(int fienum) {
		this.fienum = fienum;
	}
	
}
